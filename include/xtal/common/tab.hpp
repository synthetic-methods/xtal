template <class _T>
struct tab
{
	class type
	{
	};
	template <class S>
	class subtype: public type, public S
	{
	public:
		using S::S;
		
	};
	template <of_q<type> S>
	class subtype<S>: public S
	{
	public:
		using S::S;
		
	};
};
template <class _T>
using tab_t = typename tab<_T>::type;

template <class _T, class ...Ts>
concept tab_p = of_p<tab_t<_T>, Ts...>;


